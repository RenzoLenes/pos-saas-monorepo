import { Router } from 'express'
import { CustomerController } from '../controllers/customer.controller'

const router: Router = Router()
const controller = new CustomerController()

/**
 * @swagger
 * /api/customers/{id}/stats:
 *   get:
 *     summary: Get customer statistics
 *     description: Retrieve purchase statistics for a specific customer (total spent, purchase count, average ticket)
 *     tags: [Customers]
 *     security:
 *       - BearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *         description: Customer ID
 *         example: "clxxxxx"
 *     responses:
 *       200:
 *         description: Customer stats retrieved successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 data:
 *                   type: object
 *                   properties:
 *                     totalPurchases:
 *                       type: integer
 *                       example: 45
 *                     totalSpent:
 *                       type: number
 *                       format: decimal
 *                       example: 18750.50
 *                     averageTicket:
 *                       type: number
 *                       format: decimal
 *                       example: 416.68
 *                     lastPurchase:
 *                       type: string
 *                       format: date-time
 *                       nullable: true
 *       404:
 *         description: Customer not found
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       401:
 *         description: Unauthorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.get('/:id/stats', (req, res) => controller.getStats(req, res))

/**
 * @swagger
 * /api/customers/search:
 *   get:
 *     summary: Search customers
 *     description: Search and filter customers by name, email, or phone
 *     tags: [Customers]
 *     security:
 *       - BearerAuth: []
 *     parameters:
 *       - in: query
 *         name: q
 *         schema:
 *           type: string
 *         description: Search query (searches firstName, lastName, email, phone)
 *         example: "john"
 *       - in: query
 *         name: isActive
 *         schema:
 *           type: boolean
 *         description: Filter by active status
 *       - in: query
 *         name: page
 *         schema:
 *           type: integer
 *           default: 1
 *         description: Page number
 *       - in: query
 *         name: limit
 *         schema:
 *           type: integer
 *           default: 50
 *         description: Number of items per page
 *     responses:
 *       200:
 *         description: Customers retrieved successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 data:
 *                   type: array
 *                   items:
 *                     $ref: '#/components/schemas/Customer'
 *                 meta:
 *                   type: object
 *                   properties:
 *                     total:
 *                       type: integer
 *                     page:
 *                       type: integer
 *                     limit:
 *                       type: integer
 *       401:
 *         description: Unauthorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.get('/search', (req, res) => controller.search(req, res))

